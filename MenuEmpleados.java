    private void jButtonEdiEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEdiEmpleadoActionPerformed
            
    try {
        String id = JOptionPane.showInputDialog(this, "Diga el id del empleado a editar \n Empleados actuales: \n " + empleados);
        int idint = Integer.parseInt(id);
        boolean existe = gestion.encontrarEmpleado(idint);
        Empleado empleado = gestion.devolverEmpleado(idint);
        if (existe) {
            EditarEmpleado editar =  new EditarEmpleado(empleado);
            editar.setVisible(true);
        } else{
            JOptionPane.showMessageDialog(this, "El empleado no se puede editar, verifica el id ","",2);
        }
    } catch (SQLException ex) {
        System.err.println(ex.getMessage());
    }
        catch (NumberFormatException in){
        JOptionPane.showMessageDialog(this, "Inserte un NUMERO ","",0);
    }
    }//GEN-LAST:event_jButtonEdiEmpleadoActionPerformed

    private void jButtonEliEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliEmpleadoActionPerformed

    try {
        
        String id = JOptionPane.showInputDialog(this, "Diga el id del empleado a eliminar \n Empleados actuales: \n " + empleados);
        int idint = Integer.parseInt(id);
        boolean existe = gestion.encontrarEmpleado(idint);
        if (existe) {
            gestion.BorrarEnGestionEmpleados(idint);
            empleados.remove(idint);
        } else{
            JOptionPane.showMessageDialog(this, "El empleado no se puede editar, verifica el id ","",2);
        }
    } catch (SQLException ex) {
        System.err.println(ex.getMessage());
    }
    catch (NumberFormatException in){
        JOptionPane.showMessageDialog(this, "Inserte un NUMERO ","",0);
    }
    }//GEN-LAST:event_jButtonEliEmpleadoActionPerformed

